function reemplazarListaUnica(lista, nombre) {

	var responder = []

	for (var i = 0; i < lista.length; i++) {
		var distrito = lista[i][nombre];
		if (responder.indexOf(distrito) < 0) {
			responder.push(distrito);
		}
	}
	return responder;
}

function buscarStaff(){

	var resultado = []

	var lblResultado = document.getElementById("lblResultado")
	var inputnombre = document.getElementById("txtCuidadores").value.toLowerCase(); // Setando valor ingresado del txtbox
	var inputdistrito = document.getElementById("cmbCuidador").value; // Setando valor seleccionado / del combobox

	lblResultado.innerHTML = "";
	for (var i = 0; i < arrayCuidadores.length; i++) { 
		var cuidador = arrayCuidadores[i]; 
		
		var cuidadornombre = cuidador.nombre.toLowerCase(); 
		var cuidadordistrito = cuidador.distrito; 
		if (cuidadornombre.includes(inputnombre) && (inputdistrito == cuidadordistrito || inputdistrito == '-1')) { 
			resultado.push(cuidador);

			lblResultado.innerHTML += "<div class=\"resultado\"><img class=\"avatar\" src=\"" + path + cuidador.foto + "\"><br>NOMBRE:"+ cuidador.nombre.toUpperCase()+"<br>" + "CÓDIGO: " +cuidador.codigo+ "<br>"+ "CORREO: " +cuidador.correo.toUpperCase() + "<br>" +"TELÉFONO: " +cuidador.telefono + "<br>" + "EDAD: " +cuidador.edad +"<br>" +"DISTRITO: "+cuidador.distrito.toUpperCase() +"<br><br> </div>";
		}
	}

	if (resultado.length == 0) {
		lblResultado.innerHTML = "<div class=\"resultado\">No hay resultado para la busqueda de <b>" +inputnombre + "</b> en el distrito de <b>" +inputdistrito + "</b></div>";
	}
}





